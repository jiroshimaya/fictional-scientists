# ============================================================
# CSV 生成
# ============================================================


def _build_rows(entries: list[tuple]) -> list[dict]:
    """エントリリストから expanded CSV の行リストを生成する。"""
    from collections import defaultdict

    group_counter: defaultdict = defaultdict(int)
    rows = []
    for era_order, field, gender, nat_region, nationality in entries:
        era_name, band, start, end = ERA_META[era_order]
        group_counter[(era_name, field, nationality)] += 1
        n = group_counter[(era_name, field, nationality)]
        scientist_id = f"{era_name}__{field}__{nationality}__{n:04d}"
        rows.append(
            {
                "id": scientist_id,
                "era_order": era_order,
                "era_name": era_name,
                "birth_year_band": band,
                "birth_year_start": start,
                "birth_year_end": end,
                "field": field,
                "gender": gender,
                "nationality_region": nat_region,
                "nationality": nationality,
            }
        )
    return rows


def write_expanded_csv(output_path: pathlib.Path, rows: list[dict]) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)


def main() -> None:
    base = pathlib.Path("data/sample")

    # --- era_only_13 ---
    era_only_rows = _build_rows(ERA_ONLY_ENTRIES)
    era_only_path = base / "era_only_13" / "quota.csv"
    write_expanded_csv(era_only_path, era_only_rows)
    print(f"生成完了: {len(era_only_rows)} 件 -> {era_only_path}")

    # --- nationality_era_100 ---
    nat_era_rows = _build_rows(NATIONALITY_ERA_ENTRIES)
    nat_era_path = base / "nationality_era_100" / "quota.csv"
    write_expanded_csv(nat_era_path, nat_era_rows)
    print(f"生成完了: {len(nat_era_rows)} 件 -> {nat_era_path}")


if __name__ == "__main__":
    main()
