def _infer_nationality_region(nationality: str) -> str:
    """国籍名（nationality）から地域カテゴリを推定する。"""
    for keywords, region in _NATIONALITY_REGION_KEYWORDS:
        if any(kw in nationality for kw in keywords):
            return region
    return "西欧"  # デフォルト


def _infer_region_group(region: str) -> str:
    """地域カテゴリから服装テンプレート用の大分類グループを返す。"""
    if region in ("東地中海", "南欧"):
        return "地中海"
    elif region in ("西欧", "中欧", "東欧", "北欧", "北米", "オセアニア", "中南米"):
        return "西欧北米"
    elif region in ("東アジア", "東南アジア", "中央アジア"):
        return "東アジア"
    elif region in ("南アジア", "西アジア", "北アフリカ"):
        return "南アジア中東"
    elif region == "アフリカ":
        return "アフリカ"
    return "西欧北米"  # デフォルト


def select_appearance(nationality: str, era: str, gender: str) -> Dict[str, str]:
    """国籍・時代・性別に基づき、外見要素をランダムに選んで返す。

    Returns:
        {
            "skin_tone": str,
            "hair_color": str,
            "hair_style": str,
            "eye_shape": str,
            "face_shape": str,
            "build": str,
            "nose_shape": str,
            "beard": str,  # 男性・ノンバイナリーのみ
            "expression": str,
            "crop": str,
            "background": str,
            "lighting": str,
        }
    """
    region = _infer_nationality_region(nationality)
    region_data = _APPEARANCE_BY_REGION.get(region, _DEFAULT_APPEARANCE)

    era_gender_styles = _HAIRSTYLE_BY_ERA_GENDER.get(era, _DEFAULT_HAIRSTYLE)
    gender_key = gender if gender in era_gender_styles else "男性"
    hair_style_list = era_gender_styles.get(
        gender_key, _DEFAULT_HAIRSTYLE.get(gender_key, ["整えた短髪"])
    )

    result: Dict[str, str] = {
        "skin_tone": random.choice(
            region_data.get("skin_tone", _DEFAULT_APPEARANCE["skin_tone"])
        ),
        "hair_color": random.choice(
            region_data.get("hair_color", _DEFAULT_APPEARANCE["hair_color"])
        ),
        "hair_style": random.choice(hair_style_list),
        "eye_shape": random.choice(
            region_data.get("eye_shape", _DEFAULT_APPEARANCE["eye_shape"])
        ),
        "face_shape": random.choice(
            region_data.get("face_shape", _DEFAULT_APPEARANCE["face_shape"])
        ),
        "build": random.choice(region_data.get("build", _DEFAULT_APPEARANCE["build"])),
        "nose_shape": random.choice(
            region_data.get("nose_shape", _DEFAULT_APPEARANCE["nose_shape"])
        ),
        "expression": random.choice(_EXPRESSION_BY_ERA.get(era, ["落ち着いた表情"])),
        "crop": random.choice(_CROP_BY_ERA.get(era, ["胸から上"])),
        "background": random.choice(_BACKGROUND_BY_ERA.get(era, ["暗い無地背景"])),
        "lighting": random.choice(_LIGHTING_BY_ERA.get(era, ["柔らかい自然光"])),
    }

    if gender in ("男性", "ノンバイナリー／その他"):
        beard_list = _BEARD_BY_ERA.get(era, _DEFAULT_BEARD)
        result["beard"] = random.choice(beard_list)

    return result


def _estimate_age_impression(birth_year: Any, death_year: Any, era: str) -> str:
    """生年・没年から年齢感の表現を返す。"""
    try:
        birth = int(
            str(birth_year)
            .replace("紀元前", "-")
            .replace("年", "")
            .replace("頃", "")
            .strip()
        )
        death = int(
            str(death_year)
            .replace("紀元前", "-")
            .replace("年", "")
            .replace("頃", "")
            .strip()
        )
        # 壮年期（研究者として活躍した時期）の年齢を推定
        mid_age = (abs(death - birth) // 2) + 30
        if mid_age < 35:
            return "若々しく活力ある表情、20〜30代の若い研究者"
        elif mid_age < 50:
            return "壮年期の落ち着いた表情、30〜40代の働き盛り"
        elif mid_age < 65:
            return "中年期の円熟した表情、しわや年齢感を自然に表現、40〜50代"
        else:
            return "老齢期の深みある表情、しわや白髪を自然に表現、60代以降"
    except (ValueError, TypeError):
        return "壮年期の落ち着いた表情、知的な年齢感"


# ============================================================
# テンプレートによる portrait_prompt 直接生成（LLM不使用）
# ============================================================


def build_portrait_prompt_from_template(
    profile: Dict[str, Any], era: str, gender: str
) -> str:
    """LLMを使わず、テンプレートから直接 portrait_prompt 文字列を生成する。"""
    nationality = profile.get("国籍", "")
    field = profile.get("主な分野", "科学")

    appearance = select_appearance(nationality=nationality, era=era, gender=gender)
    age_impression = _estimate_age_impression(
        profile.get("生年"), profile.get("没年"), era
    )

    # 媒体を選択
    media_options = _TEMPLATE_MEDIA_BY_ERA.get(era, ["時代に合った肖像画"])
    selected_medium = random.choice(media_options)

    # ポーズを選択（左右方向をランダムに決定）
    direction = random.choice(["右", "左"])
    pose_options = _TEMPLATE_POSE_BY_ERA.get(era, ["{direction}向きの四分の三アングル"])
    selected_pose = random.choice(pose_options).replace("{direction}", direction)

    # 服装を選択
    region = _infer_nationality_region(nationality)
    region_group = _infer_region_group(region)
    era_clothing = _CLOTHING_BY_ERA_REGION_GENDER.get(era, {})
    group_clothing = era_clothing.get(region_group, era_clothing.get("西欧北米", {}))
    gender_key = gender if gender in group_clothing else "男性"
    clothing_options = group_clothing.get(gender_key) or ["時代・地域に合った装い"]
    selected_clothing = random.choice(clothing_options)

    # 時代ラベルと保存状態
    era_label = _ERA_LABELS.get(era, era)
    preservation_options = _PRESERVATION_BY_ERA.get(era, ["経年"])
    preservation = random.choice(preservation_options)

    # ひげ（男性・ノンバイナリーのみ）
    beard_part = f"、{appearance['beard']}" if "beard" in appearance else ""

    # 体格表記の調整（build値が「体格」で終わる場合は重複を避ける）
    build_val = appearance["build"]
    build_str = build_val if build_val.endswith("体格") else f"{build_val}の体格"

    # 表情・構図・背景・照明
    expression = appearance["expression"]
    crop = appearance["crop"]
    background = appearance["background"]
    lighting = appearance["lighting"]

    return (
        f"{nationality}出身、{era_label}の{gender}の{field}者の肖像。"
        f"{appearance['skin_tone']}、{appearance['hair_color']}で{appearance['hair_style']}、"
        f"{appearance['eye_shape']}、{appearance['nose_shape']}、{appearance['face_shape']}の顔立ち{beard_part}。"
        f"{build_str}。{age_impression}。{expression}。"
        f"{selected_clothing}。"
        f"{selected_medium}。{preservation}。"
        f"構図は{selected_pose}、{crop}。背景は{background}、照明は{lighting}。"
        f"手や顔、目、指の破綻はなく、一人だけを描く。"
        f"文字・署名・ロゴ・透かしは入れない。"
        f"過度な美化や英雄化、ファッション写真風の演出は避ける。"
        f"自然で節度ある学術人物ポートレートにする。"
    )


